let user = {};
let album = {};
let artist = {};

export function getUser() {
  return user;
}

export function setUser(newUser) {
  user = newUser;
}

export function getAlbum() {
  return album;
}

export function setAlbum(newAlbum) {
  album = newAlbum;
}

export function getArtist() {
  return artist;
}

export function setArtist(newArtist) {
  artist = newArtist;
}

